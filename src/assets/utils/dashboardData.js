export const getStatus = (status) => {
  if (status == "Completed") {
    return "success";
  } else if (status == "Not Started") {
    return "secondary";
  } else if (status == "Archived") {
    return "secondary2";
  } else if (status == "In Progress") {
    return "info";
  } else if (status == "In Review") {
    return "info2";
  } else if (status == "Delayed") {
    return "error";
  } else if (status == "On Track") {
    return "warning";
  } else if (status == "In Revision") {
    return "dark";
  }
};
